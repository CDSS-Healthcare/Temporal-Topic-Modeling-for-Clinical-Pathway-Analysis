# 1️⃣ Perplexity
def compute_perplexity(ttm, dictionary, texts):
    total_words = 0
    log_likelihood = 0.0

    # Average topic-word distributions over time bins
    phi_avg = np.mean(np.array(ttm.phi), axis=0)  # shape: (K, vocab_size)
    num_bins = len(ttm.theta)

    for d_idx, doc in enumerate(texts):
        bow = dictionary.doc2bow(doc)
        if not bow:
            continue

        total_words += sum(cnt for _, cnt in bow)

        # Collect this doc's theta from each bin where it exists
        theta_list = [
            ttm.theta[b][d_idx]
            for b in range(num_bins)
            if d_idx < len(ttm.theta[b])
        ]
        if not theta_list:
            continue

        # Average theta across bins for this document
        theta_doc = np.mean(theta_list, axis=0)  # shape: (K,)

        # Log likelihood for each word in the document
        for word_id, cnt in bow:
            pw = np.dot(theta_doc, phi_avg[:, word_id])
            if pw > 0:
                log_likelihood += cnt * np.log(pw)

    if total_words == 0:
        return 0.0

    # Gensim-style perplexity
    perplexity = np.exp(-log_likelihood / total_words)
    return perplexity


# 2️⃣ Coherence (normalized)
def compute_coherence(model, docs, dictionary, topn=20):
    top_words = []
    for k in range(model.K):
        # get sorted indices and flatten them to 1D ints
        top_indices = np.argsort(model.phi[k])[-topn:].flatten()
        words = [dictionary[int(i)] for i in top_indices]
        top_words.append(words)

    cm = CoherenceModel(
        topics=top_words,
        texts=docs,
        dictionary=dictionary,
        coherence='c_v'
    )
    return cm.get_coherence()


# 3️⃣ NKQM@N (embedding-based)
def compute_nkqm_for_ttm(model, embed_model, topn=20, Nlist=[5, 10, 15]):
    topic_topwords = {
        tid: [w for w, _ in model.get_top_words(tid, topn=topn)]
        for tid in range(model.K)
    }
    all_words = list({w for words in topic_topwords.values() for w in words})
    if not all_words:
        return {N: 0.0 for N in Nlist}

    # Embeddings
    word_embeddings = embed_model.encode(all_words, convert_to_numpy=True, show_progress_bar=False)
    word_to_emb = dict(zip(all_words, word_embeddings))

    # Relevance scores
    relevance_scores = {}
    for tid, words in topic_topwords.items():
        emb_list = [word_to_emb[w] for w in words if w in word_to_emb]
        if not emb_list:
            continue
        centroid = np.mean(emb_list, axis=0)
        for w in words:
            if w in word_to_emb:
                sim = cosine_similarity([word_to_emb[w]], [centroid])[0][0]
                relevance_scores[w] = max(sim, 0)

    def compute_normalized_nkqm(topic_topwords, relevance_scores, N):
        scores = []
        max_possible = sum(1 / np.log2(rank + 1) for rank in range(1, N + 1))
        for tid, words in topic_topwords.items():
            s = 0.0
            for rank, word in enumerate(words[:N], start=1):
                rel = relevance_scores.get(word, 0.0)
                s += rel / np.log2(rank + 1)
            scores.append(s / max_possible)
        return float(np.mean(scores)) if scores else 0.0

    return {N: compute_normalized_nkqm(topic_topwords, relevance_scores, N) for N in Nlist}


# 4️⃣ Cosine Similarity of Topic–Word Distributions across time bins (normalized)
def cosine_smoothness(ttm):
    smooth_vals = []
    for topic_id in range(ttm.K):
        n_bins = len(ttm.phi[topic_id])               # actual number of bins for this topic
        for b in range(1, n_bins):
            sim = cosine_similarity(
                [ttm.phi[topic_id][b - 1]],
                [ttm.phi[topic_id][b]]
            )[0][0]
            smooth_vals.append((sim + 1) / 2)        # normalize [-1,1] → [0,1]
    return np.mean(smooth_vals) if smooth_vals else 0.0


# 5️⃣ Jaccard similarity of top-N words across time bins (normalized)
def jaccard_stability(ttm, N=10):
    scores = []
    for topic_id in range(ttm.K):
        for b in range(1, ttm.B):
            top_prev = {w for w, _ in ttm.get_top_words(topic_id, bin_idx=b - 1, topn=N)}
            top_curr = {w for w, _ in ttm.get_top_words(topic_id, bin_idx=b, topn=N)}
            union_size = len(top_prev | top_curr)
            inter_size = len(top_prev & top_curr)
            score = inter_size / union_size if union_size > 0 else 0
            scores.append(score)
    return np.mean(scores) if scores else 0.0


# 6️⃣ DTW-based topic distribution similarity across time bins
def dtw_topic_similarity(ttm):
    """Compute average DTW distance across consecutive bins for each topic."""
    dtw_vals = []
    for topic_id in range(ttm.K):
        n_bins = len(ttm.phi[topic_id])   # actual number of available bins for this topic
        for b in range(1, n_bins):
            series_prev = ttm.phi[topic_id][b - 1]
            series_curr = ttm.phi[topic_id][b]
            dtw_dist = dtw_distance(series_prev, series_curr)
            dtw_vals.append(1 / (1 + dtw_dist))  # normalize to [0,1]
    return np.mean(dtw_vals) if dtw_vals else 0.0

