from tqdm import tqdm
class TemporalLDA:
    def __init__(self, dictionary, corpus_by_bin, K=20, alpha=0.1, beta=0.01,
                 delta=0.4, iters=150, seed=42):
        self.dictionary = dictionary
        self.id2word = dictionary.id2token
        self.V = len(dictionary)
        self.corpus_by_bin = corpus_by_bin
        self.B = len(corpus_by_bin)   # number of time bins
        self.K = K
        self.alpha = np.full(K, alpha) if np.isscalar(alpha) else np.array(alpha)
        self.beta_prior = np.full(self.V, beta) if np.isscalar(beta) else np.array(beta)
        self.delta = delta
        self.iters = iters
        self.seed = seed
        self.random = np.random.default_rng(seed)

        # Counts
        self.topic_word_counts = [np.zeros((K, self.V), dtype=np.int64) for _ in range(self.B)]
        self.topic_counts = [np.zeros(K, dtype=np.int64) for _ in range(self.B)]
        self.doc_topic_counts = []
        self.assignments = []
        self.docs_wordids = []
        self.bin_doc_index = []

        # Learned parameters
        self.phi = None   # topic-word distributions per bin
        self.theta = None # doc-topic distributions per bin
        self.final_phi = None  # final learned topic-word distribution

    def _initialize_bin(self, bin_idx, corpus_bin):
        """Randomly assign topics for a given bin and initialize counts."""
        docs_wids = []
        for bow in corpus_bin:
            wids = []
            for wid, cnt in bow:
                wids.extend([wid] * cnt)
            docs_wids.append(np.array(wids, dtype=np.int32))
        self.docs_wordids.append(docs_wids)
        self.bin_doc_index.append([])

        D = len(docs_wids)
        dtc = [np.zeros(self.K, dtype=np.int32) for _ in range(D)]
        zlist = []
        for d, wids in enumerate(docs_wids):
            z = np.empty_like(wids)
            for n, wid in enumerate(wids):
                k = self.random.integers(0, self.K)
                z[n] = k
                self.topic_word_counts[bin_idx][k, wid] += 1
                self.topic_counts[bin_idx][k] += 1
                dtc[d][k] += 1
            zlist.append(z)
        self.doc_topic_counts.append(dtc)
        self.assignments.append(zlist)

    def fit(self, doc_index_by_bin):
        """Run collapsed Gibbs sampling with temporal smoothing."""
        self.bin_doc_index = doc_index_by_bin
        for b in range(self.B):
            self._initialize_bin(b, self.corpus_by_bin[b])

        for it in tqdm(range(self.iters), desc="Training TTM"):
            for b in range(self.B):
                prev_phi = None
                if b > 0:
                    tp = self.topic_word_counts[b-1] + 1e-9
                    tp = tp / tp.sum(axis=1, keepdims=True)
                    prev_phi = tp

                for d, wids in enumerate(self.docs_wordids[b]):
                    z = self.assignments[b][d]
                    dtc = self.doc_topic_counts[b][d]
                    for n, wid in enumerate(wids):
                        k = z[n]
                        # remove
                        self.topic_word_counts[b][k, wid] -= 1
                        self.topic_counts[b][k] -= 1
                        dtc[k] -= 1

                        # conditional
                        left = (dtc + self.alpha)
                        if prev_phi is None:
                            right_num = self.topic_word_counts[b][:, wid] + self.beta_prior[wid]
                            right_den = self.topic_counts[b] + self.beta_prior.sum()
                        else:
                            right_num = self.topic_word_counts[b][:, wid] + self.beta_prior[wid] + self.delta * prev_phi[:, wid]
                            right_den = self.topic_counts[b] + self.beta_prior.sum() + self.delta
                        p = left * (right_num / np.maximum(right_den, 1e-12))
                        k_new = self.random.choice(self.K, p=p / np.sum(p))
                        z[n] = k_new

                        # add
                        self.topic_word_counts[b][k_new, wid] += 1
                        self.topic_counts[b][k_new] += 1
                        dtc[k_new] += 1

        # compute phi and theta
        self.phi = []
        self.theta = []
        for b in range(self.B):
            prev_phi = None
            if b > 0:
                tp = self.topic_word_counts[b-1] + 1e-9
                tp = tp / tp.sum(axis=1, keepdims=True)
                prev_phi = tp

            if prev_phi is None:
                pw = self.topic_word_counts[b] + self.beta_prior
            else:
                pw = self.topic_word_counts[b] + self.beta_prior + self.delta * prev_phi
            pw = pw / pw.sum(axis=1, keepdims=True)
            self.phi.append(pw)

            D = len(self.docs_wordids[b])
            thetab = np.zeros((D, self.K), dtype=np.float64)
            for d in range(D):
                counts = self.doc_topic_counts[b][d] + self.alpha
                thetab[d] = counts / counts.sum()
            self.theta.append(thetab)

        # save final phi (last time bin)
        self.final_phi = self.phi[-1]
        return self

    def get_top_words(self, topic_id=None, topn=10, bin_idx=None):
      id2word = self.dictionary.id2token

      # Select which phi (topic-word distribution) to use
      if bin_idx is not None:
          if bin_idx < 0 or bin_idx >= self.B:
              return {} if topic_id is None else []
          beta = self.phi[bin_idx]
      else:
          beta = np.mean(self.phi, axis=0) if isinstance(self.phi, list) else self.phi

      # For single topic request
      if topic_id is not None:
          if topic_id < 0 or topic_id >= self.K:
            return []

          top_indices = np.argsort(beta[topic_id])[::-1][:topn]
          return [(id2word[idx], float(beta[topic_id][idx]))
                for idx in top_indices if idx in id2word]

      # For all topics
      all_topics = {}
      for tid in range(self.K):
          top_indices = np.argsort(beta[tid])[::-1][:topn]
          all_topics[tid] = [(id2word[idx], float(beta[tid][idx]))
                          for idx in top_indices if idx in id2word]
      return all_topics


    def doc_topic_matrix(self, doc_index_by_bin):
        """Return global doc-topic matrix aligned with original doc indices."""
        D = sum(len(x) for x in doc_index_by_bin)
        M = np.zeros((D, self.K), dtype=float)
        for b in range(self.B):
            for local_idx, global_idx in enumerate(doc_index_by_bin[b]):
                M[global_idx] = self.theta[b][local_idx]
        return M

